#include "pch.h"

#include "Archive.h"

using namespace nzg;

void IvsMemFile::serialize(Archive& ar)
{
	if (ar.isStoring())
	{
		std::string str = m_ss.str();
		uint32_t len = str.length();
		ar << len;
		ar.write(str.data(), len);
	}
	else
	{
		uint32_t len;
		ar >> len;
		std::string str(len, '\0');
		ar.read((void *)str.data(), len);
		m_ss.write(str.data(), len);
		m_ss.seekg(0);

	}
}

